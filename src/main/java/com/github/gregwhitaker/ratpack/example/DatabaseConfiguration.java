package com.github.gregwhitaker.ratpack.example;

/**
 * Configuration object that holds messages to return to the user.
 */
public class DatabaseConfiguration {

    /**
     * Database host
     */
    public String host;

    /**
     * Database port
     */
    public int port;

    /**
     * Database username
     */
    public String username;

    /**
     * Database password
     */
    public String password;

    /**
     * Database name
     */
    public String database;
}
